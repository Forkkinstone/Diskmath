def solve_grid_paths():
    # Данные из условия задачи (картинка 1)
    W = 20  # Ширина (горизонталь)
    H = 17  # Высота (вертикаль)

    # --- ЧАСТЬ 1: Все кратчайшие пути ---
    # Создаем таблицу, где dp[x][y] — количество способов добраться в точку (x, y)
    dp1 = [[0] * (H + 1) for _ in range(W + 1)]
    
    for i in range(W + 1):
        for j in range(H + 1):
            if i == 0 and j == 0:
                dp1[i][j] = 1 # Начальная точка
            else:
                # Путь в текущую клетку возможен только слева или снизу
                from_left = dp1[i - 1][j] if i > 0 else 0
                from_down = dp1[i][j - 1] if j > 0 else 0
                dp1[i][j] = from_left + from_down

    total = dp1[W][H]

    # --- ЧАСТЬ 2: Ограничение (вертикальные не подряд) ---
    # Используем третье измерение для хранения состояния последнего шага:
    # state 0 — последний шаг был ГОРИЗОНТАЛЬНЫМ (или мы в начале)
    # state 1 — последний шаг был ВЕРТИКАЛЬНЫМ
    dp2 = [[[0, 0] for _ in range(H + 1)] for _ in range(W + 1)]
    
    # В начальной точке мы еще не делали шагов, поэтому считаем это "безопасным" состоянием
    dp2[0][0][0] = 1

    for i in range(W + 1):
        for j in range(H + 1):
            # 1. Пробуем шагнуть ВПРАВО (горизонтально)
            if i < W:
                # После любого предыдущего шага можно идти вправо
                dp2[i + 1][j][0] += dp2[i][j][0] + dp2[i][j][1]
            
            # 2. Пробуем шагнуть ВВЕРХ (вертикально)
            if j < H:
                # Вверх можно идти ТОЛЬКО если предыдущий шаг НЕ был вертикальным
                dp2[i][j + 1][1] += dp2[i][j][0]

    valid_paths = dp2[W][H][0] + dp2[W][H][1]

    print(f"Результаты для сетки {W}x{H}:")
    print("-" * 30)
    print("Всего кратчайших путей:", total)
    print("Путей без двух вертикальных подряд:", valid_paths)

solve_grid_paths()
